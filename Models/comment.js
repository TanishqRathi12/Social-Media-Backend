const mongoose = require('mongoose');
const { Schema } = mongoose;

const comment = new Schema({
    post: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Post',
        required: true,
    },
    author: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true,
    },
    content: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

const CommentSchema = mongoose.model('Comment', comment);

module.exports = CommentSchema;
